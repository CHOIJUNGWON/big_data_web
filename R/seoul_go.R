setwd("c:\\easy_r")
library(KoNLP)
library(wordcloud)
library(stringr)
useSejongDic()
data1<-readLines("seoul_go.txt")
data1
data2<-sapply(data1,extractNoun,USE.NAMES=F)
data2
head(unlist(data2),30)
data3<-unlist(data2)
data3<-gsub("전","",data3)
data3<-gsub("6","",data3)
data3<-gsub("7","",data3)
write(unlist(data3),"seoul.txt")
data4<-read.table("seoul.txt")
data4
nrow(data4)
wordcount<-table(data4)
wordcount
head(sort(wordcount, decreasing = T),20)
library(RColorBrewer)
palete<-brewer.pal(9,"Set3")
wordcloud(names(wordcount),freq=wordcount,scale=c(5,0.1),rot.per=0.25,min.freq=1,random.order=F,random.color=T,colors=palete)
legend(0.3,1,"서울명소",cex=0.8,fill=NA,border=NA,bg="white",text.col="red",text.font=2,box.col="red")
